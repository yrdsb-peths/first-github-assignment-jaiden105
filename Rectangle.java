public class Rectangle {
  private int width;
  private int height;

  public Rectangle(int width, int width)
  {
      this.width = width;
      this.height = height;
  }

  public int getArea()
  {
      return width * height;
  }

  public int getHeight()
  {
      return height;
  }

  public int getWidth()
  {
      return width;
  }
  
  
  
}
